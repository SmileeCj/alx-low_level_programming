#include "3-calc.h"

/**
* op_add - adding two numbers
* @a: param1
* @b: param2
*
* Description: adding numbers
*
* Return: int
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two numbers
 * @a: param1
 * @b: param2
 *
 * Description: sub numbers
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two numbers
 * @a: param1
 * @b: param2
 *
 * Description: product of numbers
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide numbers
 *
 * @a: param1
 * @b: param2
 *
 * Description: divide two numbers
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of number
 *
 * @a: param1
 * @b: param2
 *
 * Description: reminder of a number
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
